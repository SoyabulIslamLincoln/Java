public class sum_of_numbers {
    public static void main(String[] args){
        int m, n, total;
        
        n=20;
        total=0;
        
        for(m=1;m<=n;m++){
            total= total+m;
            
        }
        System.out.println("Sum is: "+total);
                
           
    }
    
}
